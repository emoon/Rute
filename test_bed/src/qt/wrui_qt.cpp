static struct PURect paint_event_rect(void* self_c) { 
    QPaintEvent* qt_data = (QPaintEvent*)self_c;
    const auto& t = qt_data->rect();
    return PURect { .x = (float)t.x(), .y = (float)t.y(), .width = (float)t.width(), .height = (float)t.height() };
}

